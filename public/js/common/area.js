function bindProvince(provinceId, cityId, quId){
	
	var pobj=document.getElementById(provinceId);
	pobj.options[0]=new Option("--请选择省份--", "");
	for(var i=0;i<province.length;i++){
		pobj.options[i+1]=new Option(province[i].name, province[i].id);
		pobj.options[i+1].title = province[i].name;
	}
	
	if(arguments.length==2){
		
		if(window.addEventListener){
			pobj.addEventListener('change',function(){provinceChange(provinceId, cityId);}, false);
		} else {
			pobj.attachEvent('onchange',function(){provinceChange(provinceId, cityId);});
		}     
	}
	
	if(arguments.length==3){
		
		if(window.addEventListener){
			pobj.addEventListener('change',function(){provinceChange(provinceId, cityId, quId);}, false);
		} else {
			pobj.attachEvent('onchange',function(){provinceChange(provinceId, cityId, quId);});
		}
		
		var cobj=document.getElementById(cityId);
		if(window.addEventListener){
			cobj.addEventListener('change',function(){ cityChange(cityId, quId);}, false);
		} else {
			cobj.attachEvent('onchange',function(){cityChange(cityId, quId);});
		}
		
		var qobj=document.getElementById(quId);
		if(window.addEventListener){
			qobj.addEventListener('change',function(){quChange(quId);}, false);
		} else {
			qobj.attachEvent('onchange',function(){quChange(quId);});
		}
	}
}

